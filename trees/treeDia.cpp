const int mxN = 2e5 + 1;

vector<int> adj[mxN];
int dia = 1;

int dfs(int node, int par){
  bool isLeaf = true;
  vector<int> childAns;
  for(auto it: adj[node]){
    if(it != par){
      isLeaf = false;
      childAns.push_back(dfs(it, node));
    }
  }
  if(isLeaf) return 1;

  sort(all(childAns));
  int n = childAns.size();
  int max1 = (n-1>=0 ? childAns[n-1] : 0);
  int max2 = (n-2>=0 ? childAns[n-2] : 0);

  int temp = 1 + max1; //node passes the info
  int itself_ans = 1 + max1 + max2; //node itself is an answer
  int ans = max(temp, itself_ans); //max possible dia of these 2
  
  dia = max(ans, dia); //update max dia for the whole tree

  return temp;
}

void solve(){
  int n;
  cin >> n;
  for(int i=2; i<=n; i++){
    int u, v;
    cin >> u >> v;
    adj[u].push_back(v);
    adj[v].push_back(u);
  }

  dfs(1, 0);
  cout << dia-1 << nl;
}
  
